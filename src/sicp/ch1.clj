(defn cubert [x]
  (cubert-iter 1.0 x))

(defn cubert-iter [guess x]
  (if (cube-good-enough? guess x)
    guess
    (cubert-iter (cube-improve guess x) x)))

(defn cube-improve [guess x]
  (/ (+ (/ x (square guess))
        (* guess 2))
     3))

(defn cube-good-enough? [guess x]
  (< (abs (- (cube guess) x)) 0.001))

(defn cube [x]
  (* x (* x x)))

(defn factorial [n]
  (loop [current n, total 1]
    (if (= current 1)
      total
      (recur (dec current)
             (* total current)))))

(defn factorial [n]
  (if (= n 1)
    1
    (* n (factorial (dec n)))))

(factorial 5)

(defn factorial-iter [n]
  (loop [product 1, counter 1, max-count n]
    (if (> counter max-count)
      product
       (recur (* counter product)
             (inc counter)
             max-count))))

 (factorial-iter 5)

